def bbox_vis(img, boxes, scores, cls_ids, conf=-1, class_names=None):

    for i in range(len(boxes)):
        box = boxes[i]
        cls_id = int(cls_ids[i])
        score = scores[i]
        if score < conf:
            continue
        x0 = int(box[0])
        y0 = int(box[1])
        x1 = int(box[2])
        y1 = int(box[3])

        color = (_COLORS[cls_id] * 255).astype(np.uint8).tolist()
        text = '{}:{:.1f}%'.format(class_names[cls_id], score * 100)
        txt_color = (0, 0, 0) if np.mean(_COLORS[cls_id]) > 0.5 else (255, 255, 255)
        font = cv2.FONT_HERSHEY_SIMPLEX

        txt_size = cv2.getTextSize(text, font, 0.4, 1)[0]
        cv2.rectangle(img, (x0, y0), (x1, y1), color, 2)

        txt_bk_color = (_COLORS[cls_id] * 255 * 0.7).astype(np.uint8).tolist()
        cv2.rectangle(
            img,
            (x0, y0 + 1),
            (x0 + txt_size[0] + 1, y0 + int(1.5*txt_size[1])),
            txt_bk_color,
            -1
        )
        cv2.putText(img, text, (x0, y0 + txt_size[1]), font, 0.4, txt_color, thickness=1)

    return img


def obb_vis(img, rboxes, scores, cls_ids, conf=-1, class_names=None, enable_put_text=False):
    for i in range(len(rboxes)):
        rbox = rboxes[i].astype(np.int32)
        ctr_x = int(np.mean(rbox[0::2]))
        ctr_y = int(np.mean(rbox[1::2]))
        cls_id = int(cls_ids[i])
        score = scores[i]
        if score < conf:
            continue
        rbox = rbox.reshape(1, -1, 2)
        color = (_COLORS[cls_id] * 255).astype(np.uint8).tolist()
        text = '{}:{:.1f}%'.format(class_names[cls_id], score * 100)
        txt_color = (0, 0, 0) if np.mean(_COLORS[cls_id]) > 0.5 else (255, 255, 255)
        font = cv2.FONT_HERSHEY_SIMPLEX
        img = np.ascontiguousarray(img, dtype=np.int32)
        cv2.polylines(img, [rbox], True, color, 2)
        if enable_put_text:
            txt_size = cv2.getTextSize(text, font, 0.4, 1)[0]
            cv2.putText(img, text, (ctr_x, ctr_y + txt_size[1]), font, 0.4, txt_color, thickness=1)

    return img

def mask_vis(img, masks, 
             scores, cls_ids, 
             bboxes=None, conf=-1, 
             class_names=None, 
             enable_put_text=True, 
             enable_put_bbox=True):
    if enable_put_text:
        assert enable_put_bbox
    for i in range(len(masks)):
        mask = masks[i].astype(np.uint8)
        cls_id = int(cls_ids[i])
        score = scores[i]
        if score < conf:
            continue
        color = (_COLORS[cls_id] * 255).astype(np.uint8)
        img = np.ascontiguousarray(img, dtype=np.int32)
        img[mask > 0] = img[mask > 0] * 0.5 + color.reshape(1, 3) * 0.5
        if enable_put_bbox and bboxes is None:
            bbox = mask_find_bboxes(mask)
            x0, y0, x1, y1 = bbox.T
            cv2.rectangle(img, (x0, y0), (x1, y1), color.tolist(), 2)
        elif enable_put_bbox:
            bbox = bboxes[i].astype(np.int32)
            x0, y0, x1, y1 = bbox.T
            cv2.rectangle(img, (x0, y0), (x1, y1), color.tolist(), 2)
        if enable_put_text:
            text = '{}:{:.1f}%'.format(class_names[cls_id], score * 100)
            txt_color = (0, 0, 0) if np.mean(_COLORS[cls_id]) > 0.5 else (255, 255, 255)
            font = cv2.FONT_HERSHEY_SIMPLEX
            txt_size = cv2.getTextSize(text, font, 0.4, 1)[0]
            cv2.rectangle(
                img,
                (x0, y0 + 1),
                (x0 + txt_size[0] + 1, y0 + int(1.5*txt_size[1])),
                txt_color,
                1
            )
            cv2.putText(img, text, (x0, y0 + txt_size[1]), font, 0.4, txt_color, thickness=1)

    return img

